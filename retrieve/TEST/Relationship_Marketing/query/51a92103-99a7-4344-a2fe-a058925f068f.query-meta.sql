    s.TriggeredSendCustomerKey,
    s.ListID,
    s.BatchID,
    s.SubscriberID,
    s.SubscriberKey,
    s.EventDate,
    sub.EmailAddress,
    sub.Status,
    sub.CreatedDate SubCreatedDate
FROM
    _Sent s
    LEFT JOIN _Job j ON s.JobID = j.JobID
    LEFT JOIN ENT._ListSubscribers Sub ON s.SubscriberID = sub.SubscriberID
WHERE
    s.EventDate >= DATEADD(DAY, -11, GETDATE())
