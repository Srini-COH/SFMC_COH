    j.EmailSubject,
    j.IsWrapped,
    j.TestEmailAddr,
    j.Category,
    j.BccEmail,
    j.OriginalSchedTime,
    j.CreatedDate,
    j.CharacterSet,
    j.IPAddress,
    j.SalesForceTotalSubscriberCount,
    j.SalesforceErrorSubscriberCOunt,
    j.SendType,
    j.DynamicEmailSubject,
    j.SuppressTracking,
    j.SendClassificationType,
    j.SendClassification,
    j.ResolveLinksWithCurrentData,
    j.EmailSendDefinition,
    j.DeduplicateByEmail,
